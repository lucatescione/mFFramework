using System;

namespace mFFramework.Types
{

    /// <summary>
    /// Versione del mfFramework
    /// </summary>
    public class mFVersion
    {

        /// <summary>
        /// Nome completo del mFFramework
        /// </summary>
        public string FullName { get; set; }
        /// <summary>
        /// Descrizione del mFFramework
        /// </summary>
        public string Description { get; set; }
        /// <summary>
        /// Numero di versione del mFFRamework
        /// </summary>
        public string NumberVersion { get; set; }
        /// <summary>
        /// Sviluppatore del mFFramework
        /// </summary>
        public string Company { get; set; }
        /// <summary>
        /// Copyright
        /// </summary>
        public string CopyRight { get; set; }
        /// <summary>
        /// Data della versione corrente del mFFramework
        /// </summary>
        public DateTime DateCreation { get; set; }



    }
}
